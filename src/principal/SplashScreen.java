    public void startThread(){
        Thread thread = new Thread(new Runnable() {
            @Override
            //Chamada do formulário de login
            public void run() {
                Login login = new Login(spl);
                //Executa no centro da tela
                login.setLocationRelativeTo(null);
                login.setVisible(true);
                //fecha a tela de login para, depois abrir o splash com o progresso
                dispose();
            }
        });
        
        thread.start();
    }
    
