    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        if (tabela.getRowCount() == 0){
            JOptionPane.showMessageDialog(null, "Informe, pelo menos, um produto", "Erro", JOptionPane.ERROR_MESSAGE);
        } else if (txtRecebido.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Insira um valor monetário no campo Recebido", "Cobrança", JOptionPane.WARNING_MESSAGE);
        } else {
            //foi inserido um valor válido
            float valorRecebido = Float.parseFloat(txtRecebido.getText());
            float valorTotal = Float.parseFloat(txtTotal.getText());
            
            if (valorRecebido < valorTotal){
                JOptionPane.showMessageDialog(null, "Valor insuficiente para essa transação", "Erro", JOptionPane.ERROR_MESSAGE);
            } else {
                float troco = valorRecebido - valorTotal;
                DecimalFormat formato = new DecimalFormat("#.##");
                txtTroco.setText(String.valueOf(formato.format(troco)));
            }
            
        }     
    }//GEN-LAST:event_btnCalcularActionPerformed

    private void btnVenderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVenderActionPerformed
        if (tabela.getRowCount() == 0){
            JOptionPane.showMessageDialog(null, "Erro ao realizar venda\nNão existem produtos no caixa", "ERRO", JOptionPane.ERROR_MESSAGE);
        }
        else if (txtRecebido.getText().equals("") || txtRecebido.getText().equals(null)){
            JOptionPane.showMessageDialog(null, "É necessário efetuar o pagamento das compras", 
                    "Efetue o Pagamento", JOptionPane.ERROR_MESSAGE);
        }
        else {
            float valorRecebido = Float.parseFloat(txtRecebido.getText());
            float valorTotal = Float.parseFloat(txtTotal.getText());
            if (valorTotal > valorRecebido){
                JOptionPane.showMessageDialog(null, "Valor insuficiente para essa transação.\nInsira novamente o valor",
                        "ERRO", JOptionPane.ERROR_MESSAGE);
            } else {
                Vendas venda = new Vendas();
                venda.setCodigo_venda(Integer.parseInt(txtNumeroVenda.getText()));
                venda.setTotal_venda(Float.parseFloat(txtTotal.getText()));
                venda.setData_venda(txtData.getText());
                int idSessaoUsuario = Sessao.armazenarIdUsuario(Sessao.usuario);
                venda.setId_usuario(idSessaoUsuario);

                int acao = VendasDAO.cadastrar(venda);
                if (acao != 0) {
                    limparCampos();
                    JOptionPane.showMessageDialog(null, "Venda efetuada com sucesso", "VENDA", JOptionPane.INFORMATION_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_btnVenderActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if (tabela.getRowCount() > 0){
            DefaultTableModel modelo = (DefaultTableModel) tabela.getModel();
            int linha = tabela.getSelectedRow();
            if (linha > 0){
                modelo.removeRow(linha);
                FrmListaProd listaProd = new FrmListaProd();
                listaProd.calcular();
            } else {
                JOptionPane.showMessageDialog(this, "Selecione um produto para excluir", 
                        "Registro não selecionado", JOptionPane.WARNING_MESSAGE);
            }
            
        } else {
            JOptionPane.showMessageDialog(this, "Não há produtos no caixa", 
                    "Erro ao efetuar a exclusão", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

