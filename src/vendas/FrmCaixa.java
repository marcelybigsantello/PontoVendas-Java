    public boolean estaFechado(Object obj){
        JInternalFrame ativos[] = carregador.getAllFrames();
        boolean fechado = true;
        int i = 0;
        while (i < ativos.length && fechado){
            if (ativos[i] == obj){
                fechado = false;
            }
            i++;
        }
        return fechado;
    }
    
