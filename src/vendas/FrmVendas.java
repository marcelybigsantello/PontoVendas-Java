    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if (tabela.getRowCount() > 0){
            if (tabela.getSelectedRow() > 0){
                int resposta = JOptionPane.showConfirmDialog(this, "Deseja realmente excluir a venda?", 
                        "Confirmação exclusão venda", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
                if (resposta == JOptionPane.YES_OPTION){
                    int linha = tabela.getSelectedRow();
                    //int id = (int) tabela.getValueAt(linha, 0);
                    int id = Integer.valueOf((String)tabela.getValueAt(linha, 0));
                    int excluir = VendasDAO.excluir(id);
                    if (excluir != 0 ){
                        limparCampos();
                        JOptionPane.showMessageDialog(this, "Venda excluída com sucesso", 
                                "Exclusão de venda", JOptionPane.INFORMATION_MESSAGE);
                    }    
                }
            } else {
                JOptionPane.showMessageDialog(this, "Selecione um registro para excluir", 
                        "Exclusão de Venda", JOptionPane.ERROR_MESSAGE);
            } 
        } else {
            JOptionPane.showMessageDialog(this, "Não há registros de vendas na tabela", 
                    "Sem registros na Base", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnExcluirTudoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirTudoActionPerformed
        if (tabela.getRowCount() > 0){
            int resposta = JOptionPane.showConfirmDialog(this, "Deseja realmente excluir\n\btodos\b os registros de vendas?", "Confirmação exclusão vendas",
                    JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE );
            if (resposta == JOptionPane.YES_OPTION){
                int excluirTudo = VendasDAO.excluirTudo();
                if (excluirTudo != 0){
                    limparCampos();
                    JOptionPane.showMessageDialog(this, "Todas as vendas excluídas com sucesso",
                            "Exclusão de vendas", JOptionPane.INFORMATION_MESSAGE);
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Não há registros de vendas na tabela", 
                    "Sem registros na Base", JOptionPane.WARNING_MESSAGE);
        }    
    }//GEN-LAST:event_btnExcluirTudoActionPerformed

