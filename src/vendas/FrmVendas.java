    private void btnBuscarPorDataActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarPorDataActionPerformed
        if (dateData.getDate() == null){
            VendasDAO.listarVendasCliente("");
        } else {
            // usuário preencheu o filtro data
            String formato = dateData.getDateFormatString();
            Date data = dateData.getDate();
            SimpleDateFormat sdf = new SimpleDateFormat(formato);
            VendasDAO.listarPorData(sdf.format(data));
        }
        
    }//GEN-LAST:event_btnBuscarPorDataActionPerformed

